namespace DotNet.Kyu6;

public class MiniStringFook
{
    public static string MyFirstInterpreter(string code)
    {
        string output = "";
        byte memory = 0;

        foreach (var c in code)
        {
            switch (c)
            {
                case '+':
                    memory++;
                    break;
                case '.':
                    output += (char)memory;
                    break;
            }
        }
        
        return output;
    }

